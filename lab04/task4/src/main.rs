fn my_first_interpreter(code: &str) -> String {
    let mut state: u8 = 0;
    let mut string = "".to_string();

    for ch in code.chars() {
        if ch == '+' {
            state = state.wrapping_add(1);
        } else if ch == '.' {
            string.push(state as char);
        }
    }
    string
}

